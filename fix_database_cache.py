from pathlib import Path
import os


def clear_python_cache():
    """Clear Python cache files"""

    print("ðŸ§¹ Clearing Python cache...")

    current_dir = Path.cwd()
    cache_dirs = []

    # Find all __pycache__ directories
    for root, dirs, files in os.walk(current_dir):
        if '__pycache__' in dirs:
            cache_path = Path(root) / '__pycache__'
            cache_dirs.append(cache_path)

    # Remove cache directories
    for cache_dir in cache_dirs:
        try:
            shutil.rmtree(cache_dir)
            print(f"   âœ… Removed: {cache_dir}")
        except Exception as e:
            print(f"   âš ï¸ Could not remove {cache_dir}: {e}")

    # Remove .pyc files
    pyc_files = []
    for root, dirs, files in os.walk(current_dir):
        for file in files:
            if file.endswith('.pyc'):
                pyc_path = Path(root) / file
                pyc_files.append(pyc_path)

    for pyc_file in pyc_files:
        try:
            pyc_file.unlink()
            print(f"   âœ… Removed: {pyc_file}")
        except Exception as e:
            print(f"   âš ï¸ Could not remove {pyc_file}: {e}")

    print(f"ðŸ§¹ Cache cleanup complete: {len(cache_dirs)} dirs, {len(pyc_files)} files")


